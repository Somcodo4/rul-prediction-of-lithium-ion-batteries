# Define a function to calculate the sum of three numbers
def calculate_sum(a, b, c):
    return a + b + c

# Define three numbers
number1 = 5
number2 = 6
number3 = 4

# Calculate the sum
result = calculate_sum(number1, number2, number3)

# Print the result
print(f"The sum of {number1} and {number2} and {number3} is {result}")
